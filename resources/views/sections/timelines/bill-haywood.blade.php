    <div class="timeline-list">
        <div class="timeline-item">
            <div class="timeline-dot"></div>
            <div class="timeline-date">Feb 4, 1869</div>
            <div class="timeline-title">Born in Salt Lake City, Utah Territory</div>
            <div class="timeline-body">William Dudley "Big Bill" Haywood. Lost his right eye in a childhood whittling accident around age nine.</div>
        </div>

        <div class="timeline-item">
            <div class="timeline-dot"></div>
            <div class="timeline-date">1896</div>
            <div class="timeline-title">Joined the Western Federation of Miners</div>
            <div class="timeline-body">Signed on with the WFM in Silver City, Idaho, while working the Blaine mine.</div>
        </div>

        <div class="timeline-item">
            <div class="timeline-dot"></div>
            <div class="timeline-date">1900</div>
            <div class="timeline-title">Elected Secretary-Treasurer of the WFM</div>
            <div class="timeline-body">Moved to WFM headquarters in Denver and became the union's most prominent national organizer.</div>
        </div>

        <div class="timeline-item">
            <div class="timeline-dot"></div>
            <div class="timeline-date">1903&ndash;1904</div>
            <div class="timeline-title">Colorado Labor Wars / Cripple Creek strike</div>
            <div class="timeline-body">WFM leadership besieged by the Citizens' Alliance and the state militia. Brief detentions of Haywood and other WFM officers; no formal charges that stuck.</div>
        </div>

        <div class="timeline-item">
            <div class="timeline-dot"></div>
            <div class="timeline-date">June 27, 1905</div>
            <div class="timeline-title">Co-founded the Industrial Workers of the World</div>
            <div class="timeline-body">Opened the IWW founding convention in Chicago&mdash;"the Continental Congress of the working class"&mdash;alongside Eugene Debs, Mother Jones, Lucy Parsons, and Daniel De Leon.</div>
        </div>

        <div class="timeline-item timeline-item--imprisoned">
            <div class="timeline-dot"></div>
            <div class="timeline-date">Feb 17, 1906 &mdash; Jul 29, 1907 &middot; 528 days</div>
            <div class="timeline-title">Ada County Jail, Boise &mdash; Steunenberg assassination case</div>
            <div class="timeline-body">Pinkerton detectives, acting on a coerced confession from Harry Orchard, kidnapped Haywood, Charles Moyer, and George Pettibone in Denver and put them on a special train to Idaho with no extradition hearing. Charged with conspiracy to murder former Idaho Governor Frank Steunenberg, killed by a bomb at his Caldwell home Dec 30, 1905. Ran for Colorado governor on the Socialist ticket from his cell in 1906. Trial opened May 9, 1907 before Judge Fremont Wood; defended by Clarence Darrow against prosecutors James Hawley and Sen. William Borah. Acquitted by jury after nine hours of deliberation.</div>
        </div>

        <div class="timeline-item">
            <div class="timeline-dot"></div>
            <div class="timeline-date">1912</div>
            <div class="timeline-title">Lawrence textile strike (Massachusetts)</div>
            <div class="timeline-body">Led the "Bread and Roses" strike with Elizabeth Gurley Flynn. Was not jailed; the criminal-conspiracy prosecution targeted Joe Ettor, Arturo Giovannitti, and Joseph Caruso.</div>
        </div>

        <div class="timeline-item timeline-item--imprisoned">
            <div class="timeline-dot"></div>
            <div class="timeline-date">Spring 1913</div>
            <div class="timeline-title">Paterson silk strike (New Jersey)</div>
            <div class="timeline-body">Brief jailings in Passaic County alongside ~1,850 other strikers. Free again by May 1913 to speak at the Madison Square Garden Pageant. Specific dates not preserved in surviving records.</div>
        </div>

        <div class="timeline-item">
            <div class="timeline-dot"></div>
            <div class="timeline-date">1914</div>
            <div class="timeline-title">Elected General Secretary-Treasurer of the IWW</div>
            <div class="timeline-body">Took over national leadership of the union from Vincent St. John.</div>
        </div>

        <div class="timeline-item timeline-item--imprisoned">
            <div class="timeline-dot"></div>
            <div class="timeline-date">Sep 5, 1917</div>
            <div class="timeline-title">Federal raids on IWW halls nationwide</div>
            <div class="timeline-body">Justice Department agents raided 48 IWW offices across the country. Haywood was indicted with 165 other Wobblies under the Espionage Act of 1917 for opposing the U.S. entry into World War I and obstructing the draft.</div>
        </div>

        <div class="timeline-item timeline-item--imprisoned">
            <div class="timeline-dot"></div>
            <div class="timeline-date">Apr &ndash; Aug 1918</div>
            <div class="timeline-title"><em>United States v. Haywood et al.</em></div>
            <div class="timeline-body">Mass trial in Chicago before Judge Kenesaw Mountain Landis. 101 defendants in the dock. Convicted Aug 17, 1918; Haywood sentenced to 20 years federal prison and a $20,000 fine.</div>
        </div>

        <div class="timeline-item timeline-item--imprisoned">
            <div class="timeline-dot"></div>
            <div class="timeline-date">1918&ndash;1919 &middot; ~318 days</div>
            <div class="timeline-title">USP Leavenworth</div>
            <div class="timeline-body">Held at the Leavenworth federal penitentiary while his appeal was pending. Released on bond in 1919.</div>
        </div>

        <div class="timeline-item timeline-item--exile">
            <div class="timeline-dot"></div>
            <div class="timeline-date">Apr 1921</div>
            <div class="timeline-title">Jumped bail; fled to Soviet Russia</div>
            <div class="timeline-body">After the Supreme Court denied his appeal and he was ordered to surrender, Haywood was smuggled out of the United States as a stowaway. He never returned.</div>
        </div>

        <div class="timeline-item timeline-item--exile">
            <div class="timeline-dot"></div>
            <div class="timeline-date">1921&ndash;1928</div>
            <div class="timeline-title">Exile in the Soviet Union</div>
            <div class="timeline-body">Worked at the Kuzbass Autonomous Industrial Colony in Siberia. Later moved to Moscow as his health declined from diabetes and a stroke.</div>
        </div>

        <div class="timeline-item timeline-item--death">
            <div class="timeline-dot"></div>
            <div class="timeline-date">May 18, 1928</div>
            <div class="timeline-title">Died in Moscow at age 59</div>
            <div class="timeline-body">Half of his ashes were interred in the Kremlin Wall Necropolis; the other half were sent to Forest Home (Waldheim) Cemetery in Forest Park, Illinois, near the graves of the Haymarket martyrs.</div>
        </div>
    </div>
</div>
